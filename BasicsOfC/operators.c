#include <stdio.h>

int main(){
    int a, b;
    a = 20;
    b = 6;
    
    printf("Arithmetical Operators");
    printf("a + b = %d \n", a + b);
    printf("a - b = %d \n", a - b);
    printf("a * b = %d \n", a * b);
    printf("a / b = %d \n", a / b);

    printf("Relational Operators");
    printf("a==b is %d\n", a==b); 

    return 0;
}
